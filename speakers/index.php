<div class="vcard">
<a href="http://www.flickr.com/photos/andybudd/238893167/" class="speakerpic thomas"><img class="photo" src="/images/thomas.jpg" width="150" height="180" alt="Thomas Vander Wal" /></a>
<h4 id="thomas"><span class="fn">Thomas Vander Wal</span> <a class="url" href="http://www.vanderwal.net/">VanderWal.net/</a></h4>
</div>

<p class="floatfix">Thomas Vander Wal is a popular speaker on tagging/folksonomy, social web, and web applications around well structured information.  He is often recognized as the person who coined the term &#8216;folksonomy&#8217; in 2004, as well as some of his other terms: Personal InfoCloud, Local InfoCloud, Come to Me Web, digital model of attraction etc. Thomas is founder, principal, and senior consultant at <a href="http://www.infocloudsolutions.com/">InfoCloud Solutions</a>, a social web consulting firm.  Thomas has been working professionally on the web since 1995 (with professional IT background beginning in 1988) and has breadth and depth across many roles and disciplines around web design, social web development &amp; research and general web development. He is a member of the Web Standards Project Steering Committee and helped found the Information Architecture Institute and Boxes &amp; Arrows web magazine.</p>

<p class="vevent"><strong>Thomas is presenting <em><a class="summary" href="/workshops/#thomas">Building the Social Web with Tagging/Folksonomies</a></em>, <abbr class="dtstart" title="2007-09-06">6th September</abbr></strong></p>


