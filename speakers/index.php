<div class="vcard">
<a href="http://www.flickr.com/photos/wordridden/279242723/" class="speakerpic jeremy"><img class="photo" src="/images/jeremy.jpg" width="150" height="180" alt="Jeremy Keith" /></a>
<h4 id="jeremy"><span class="fn">Jeremy Keith</span> <a class="url" href="http://adactio.com/">Adactio.com</a></h4>
</div>

<p class="floatfix">Jeremy Keith is a web developer living and working in Brighton, England. Working with the web consultancy firm <a href="http://clearleft.com/">Clearleft</a>, Jeremy enjoys building accessible, elegant websites using the troika of web standards: XHTML, CSS and the DOM.</p>

<p>Jeremy is the author of <em><a href="http://domscripting.com/">DOM Scripting: Web Design with JavaScript and the Document Object Model</a></em>. He is a member of the <a href="http://www.webstandards.org/">Web Standards Project</a> where he serves as joint leader of the DOM Scripting Task Force.</p>

<p>When he&#8217;s not building websites, Jeremy plays bouzouki in the alt.country band, <a href="http://www.saltercane.com/">Salter Cane</a>. He is also the creator and curator of <a href="http://www.thesession.org/">The Session</a>, one of the Web&#8217;s largest online communities dedicated to Irish traditional music.</p>

<div class="vcard">
<a href="http://www.flickr.com/photos/adactio/464949999/" class="speakerpic tantek"><img class="photo" src="/images/tantek.jpg" width="150" height="180" alt="Tantek &#199;elik" /></a>
<h4 id="tantek"><span class="fn">Tantek &#199;elik</span> <a class="url" href="http://tantek.com/">Tantek.com</a></h4>
</div>

<p class="floatfix">Tantek &#199;elik is Chief Technologist at Technorati where he leads the design and development of new standards and technologies. Prior to Technorati, he was a veteran representative to the World Wide Web Consortium (W3C) for Microsoft, where he also helped lead the development of the award-winning Internet Explorer for Macintosh.

As co-founder of the <a href="http://microformats.org/">microformats.org</a> community and the Global Multimedia Protocols Group, as well as Steering Committee member of the Web Standards Project and invited expert to the W3C Cascading Style Sheets working group, Tantek is dedicated to advancing open standards and simpler data formats for the Web.</p>

<p class="vevent"><strong>Jeremy &amp; Tantek are presenting <em><a class="summary" href="/workshops/#jeremytantek">Microformats</a></em>, <abbr class="dtstart" title="2007-09-05T10:00">5th September</abbr></strong></p>

