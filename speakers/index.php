  <h3>Speakers</h3>
  
<hr />

<div class="vcard">
<a href="http://www.flickr.com/photos/jesper/267786169/" class="speakerpic jared"><img class="photo" src="/images/jared.jpg" width="150" height="180" alt="Jared Spool" /></a>
<h4 id="jared"><span class="fn">Jared Spool</span> <a class="url" href="http://www.uie.com/">UIE.com</a></h4>
</div>

<p class="floatfix">If you&#8217;ve ever seen Jared speak about usability, you know that he&#8217;s probably the most effective, knowledgeable communicator on the subject today. What you probably don&#8217;t know is that he has guided the research agenda and built User Interface Engineering into the largest research organization of its kind in the world. He&#8217;s been working in the field of usability and design since 1978, before the term &#8216;usability&#8217; was ever associated with computers.</p>

<p>Jared spends his time working with the research teams at the company; he helps clients understand how to solve their design problems, and explains to reporters and industry analysts what the current state of design is all about. Jared is a top-rated speaker, and presents at over 20 conferences every year, and is the conference chair and keynote speaker at the annual User Interface Conference. Jared is on the faculty of the Tufts University Gordon Institute, and even manages to squeeze in a fair amount of writing time.</p>

<p class="vevent"><strong>Jared is presenting <em><a class="summary" href="/schedule/#jared">The Dawning of the Age of Experience</a></em>, <abbr class="dtstart" title="2007-09-07T10:00">10:00</abbr>&ndash;<abbr class="dtend" title="2007-09-07T10:45">10:45</abbr></strong></p>

<hr />

<div class="vcard">
<a href="http://www.flickr.com/photos/_leisa/262945227/" class="speakerpic leisa"><img class="photo" src="/images/leisa.jpg" width="150" height="180" alt="Leisa Reichelt" /></a>
<h4 id="leisa"><span class="fn">Leisa Reichelt</span> <a class="url" href="http://www.disambiguity.com/">Disambiguity.com</a></h4>
</div>

<p class="floatfix">Leisa is a Principal Consultant at Flow Interactive in London, where she practises contextual research and user centred design for clients including Transport for London, the BBC and Moo Print. With a background in Information Architecture and Project Management Leisa has been designing online experiences for more than a decade.</p>

<p class="vevent"><strong>Leisa is presenting <em><a class="summary" href="/schedule/#leisa">Waterfall Bad, Washing Machine Good</a></em>, <abbr class="dtstart" title="2007-09-07T11:15">11:15</abbr>&ndash;<abbr class="dtend" title="2007-09-07T12:00">12:00</abbr></strong></p>

<hr />

<div class="vcard">
<a href="http://www.flickr.com/photos/brianoberkirch/116668136/" class="speakerpic peter"><img class="photo" src="/images/peter.jpg" width="150" height="180" alt="Peter Merholz" /></a>
<h4 id="peter"><span class="fn">Peter Merholz</span> <a class="url" href="http://peterme.com/">PeterMe.com</a></h4>
</div>

<p class="floatfix">Peter Merholz is President of Adaptive Path, an experience strategy and design firm based in San Francisco. He is an experienced information architect, writer, speaker and leader in the field of designing for user experience. Peter is a regular speaker at Web design and information architecture conferences, including SxSW and the IA Summit.</p>

<p>Peter is perhaps best known for his blog, published since 1998, where he writes about design, business, and technology. In fact, Peter was the person who coined the term &#8216;blog&#8217; in the first place.</p>

<p>Peter is a passionate teacher, and has travelled throughout the United States and Europe giving one- and two-day workshops on user experience methods and fundamentals.</p>

<p class="vevent"><strong>Peter is presenting <em><a class="summary" href="/schedule/#peter">Experience Strategies</a></em>, <abbr class="dtstart" title="2007-09-07T12:00">12:00</abbr>&ndash;<abbr class="dtend" title="2007-09-07T12:45">12:45</abbr></strong></p>

<hr />

<div class="vcard">
<a href="http://www.flickr.com/photos/andybudd/170340546/" class="speakerpic cameron"><img src="/images/cameron.jpg" width="150" height="180" alt="Cameron Moll" /></a>
<h4 id="cameron"><span class="fn">Cameron Moll</span> <a class="url" href="http://cameronmoll.com/">CameronMoll.com</a></h4>
</div>

<p class="floatfix">Co-author of <em><a href="http://cssmastery.com/">CSS Mastery</a></em> and author of <em><a href="http://mobilewebbook.com/">Mobile Web Design</a></em>, Cameron Moll creates meaningful web interfaces that harmonize utility and presentation. His work and advice has been featured by Forrester Research, Communication Arts, National Public Radio (NPR), and many others. He speaks on user interface design at conferences throughout the world, and amid all this craziness he still finds time to play ball with each of his boys.</p>

<p>Cameron is currently the Interaction Design Manager for the LDS Church, helping manage the many websites and applications of a organization with more than 12 million members worldwide. Cameron resides in Salt Lake City, Utah with his wife Suzanne and four sons.</p>

<p class="vevent"><strong>Cameron is presenting <em><a class="summary" href="/schedule/#cameron">Good vs. Great Design</a></em>, <abbr class="dtstart" title="2007-09-07T14:00">14:00</abbr>&ndash;<abbr class="dtend" title="2007-09-97T14:45">14:45</abbr></strong></p>

<hr />

<div class="vcard">
<a href="http://www.flickr.com/photos/laughingsquid/107128394/" class="speakerpic george"><img class="photo" src="/images/george.jpg" width="150" height="180" alt="George Oates" /></a>
<h4 id="george" class="aname"><span class="fn">George Oates</span> <a class="url" href="http://george08.blogspot.com/">george08.blogspot.com</a></h4>
</div>

<p class="floatfix">Principal designer at <a href="http://flickr.com/">Flickr</a>, George is an online application designer fascinated by the human condition. She is a founding member of the award-winning team that built Flickr, and finds herself constantly delighted by things like user-contributed content, interaction design, organic information systems and sunsets.</p>

<div class="vcard">
<a href="http://www.flickr.com/photos/plasticbag/466242646/" class="speakerpic denise"><img class="photo" src="/images/denise.jpg" width="150" height="180" alt="Denise Wilton" /></a>
<h4 id="denise"><span class="fn">Denise Wilton</span> <a class="url" href="http://www.kaiusdesign.com/">KaiusDesign.com</a></h4>
</div>

<p class="floatfix">Denise Wilton is the graphic designer at UK startup <a href="http://moo.com/">moo.com</a>. She looks after the overall look-and-feel, from online graphics to print and packaging. Denise is also the &#8216;community manager&#8217;, keeping a keen eye on partnerships and developing communities based around the MOO products. Prior to MOO, she was variously self employed, a Creative Director at Emap (UK publishers), and senior designer at Wheel. Denise is a co-founder of <a href="http://b3ta.com/">b3ta</a> and has written articles for MacFormat, Computer Arts and Design Week.</p>

<p class="vevent"><strong>George &amp; Denise are presenting <em><a class="summary" href="/schedule/#georgedenise">Human Traffic</a></em>, <abbr class="dtstart" title="2007-09-07T14:45">14:45</abbr>&ndash;<abbr class="dtend" title="2007-09-07T15:30">15:30</abbr></strong></p>

<hr />

<div class="vcard">
<a href="http://www.flickr.com/photos/plasticbag/496592772/" class="speakerpic matt"><img class="photo" src="/images/matt.jpg" width="150" height="180" alt="Matt Webb" /></a>
<h4 id="matt"><span class="fn">Matt Webb</span> <a class="url" href="http://interconnected.org/home">interconnected.org/home</a></h4>
</div>

<p class="floatfix">Matt Webb is a principal of the creative design consultancy <a href="http://schulzeandwebb.com">Schulze &amp; Webb</a> where his work has included material prototypes for Nokia, web strategy for the BBC, and exploration into the future uses of RFID. <abbr title="Schulze &amp; Webb">S&amp;W</abbr> works in near-term product <abbr title="Research &amp; Development">R&amp;D</abbr> and, as embodied in the USB puppet <a href="http://availabot.com">Availabot</a>, it has a special focus on the social life of stuff. Matt speaks on interaction design and technology, is co-author of <em><a href="http://mindhacks.com/book">Mind Hacks</a></em>, cognitive psychology for a general audience, and builds polite social software and Web toys. He can be found at <a href="http://interconnected.org/home">Interconnected</a> and in London.</p>

<p class="vevent"><strong>Matt is presenting <em><a class="summary" href="/schedule#matt">The Experience Stack</a></em>, <abbr class="dtstart" title="2007-09-07T16:00">16:00</abbr>&ndash;<abbr class="dtend" title="2007-09-07T16:45">16:45</abbr></strong></p>

<hr />

<div class="vcard">
<a href="http://www.flickr.com/photos/bees/256261074/" class="speakerpic tom"><img class="photo" src="/images/tom.jpg" width="150" height="180" alt="Tom Coates" /></a>
<h4 id="tom"><span class="fn">Tom Coates</span> <a class="url" href="http://plasticbag.org/">PlasticBag.org</a></h4>
</div>

<p class="floatfix">Tom Coates works for Yahoo Brickhouse where he develops new concepts in social software, future media and the web of data. He focuses on the shape of the web to come and how to make things that thrive as part of it. He&#8217;s worked for many of the UK&#8217;s most prominent web companies including Time Out, UpMyStreet and the BBC where he ran a small near-term <abbr title="Research &amp; Development">R&amp;D</abbr> team for the BBC exploring media navigation, annotation and distribution.</p>

<p>A regular speaker at conferences including ETech, XTech, IASummit and The Future of Web Apps, Tom also writes extensively at plasticbag.org as well as running the experimental online community <a href="http://barbelith.com/">barbelith.com</a>.</p>

<p class="vevent"><strong>Tom is presenting <em><a class="summary" href="/schedule/#tom">Designing for a Web of Data</a></em>, <abbr class="dtstart" title="2007-09-07T16:45">16:45</abbr>&ndash;<abbr class="dtend" title="2007-09-07T17:30">17:30</abbr></strong></p>

