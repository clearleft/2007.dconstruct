<h4 id="jeremytantek"><span class="time">Wednesday 5th September /</span> Microformats <span class="by">by</span> <a href="/speakers/#jeremy">Jeremy Keith</a> <span class="by">&amp;</span> <a href="/speakers/#tantek">Tantek &#199;elik</a></h4>
<h5 class="soldout">Sold out</h5>

<p>This workshop will cover just about everything to do with microformats. That&#8217;s the beauty of such a simple technology: it only takes a single day to become an expert. This won&#8217;t be a passive presentation: you will learn not just the theory of microformats; you&#8217;ll also get down and dirty with some hands-on markup.</p>

<ul>
 <li><strong>What are microformats?</strong> Get a good grounding in the kind of problems microformats are designed to solve... and find out where the limits of microformats lie (by design). You&#8217;ll also find out exactly how microformats are created and learn how you can contribute to the their ongoing evolution.</li>
 <li class="bul2"><strong>Laying the foundations for microformats:</strong> it&#8217;s important to understand the value of meaningful markup before implementing microformats. This will get you thinking about how you should be marking up your content using plain ol&#8217; semantic HTML before adding any microformat magic.</li>
 <li><strong>Parade of the microformats:</strong> from simple elemental microformats that consist of nothing more than adding an attribute, right up to compound microformats that can be mashed up together, you&#8217;ll meet them all.</li>
 <li class="bul2"><strong>Smell the microformats.</strong> Just as you can develop a nose for wine tasting, you can also develop a sense for spotting which content is just crying out to be microformatted. Once you&#8217;ve learned to see these patterns, you&#8217;ll never surf the web the same way again.</li>
 <li><strong>Add microformats to your sites.</strong> Using a combination of exercises, this workshop will teach you to how to spice up your content with semantic goodness. If you&#8217;re up to the challenge, you&#8217;ll have your site fully microformatted by the end of the day.</li></ul>


<hr />


 <h4 id="leisa"><span class="time">Wednesday 5th September /</span> Collaboration, Creativity &amp; Consensus in User Experience Design <span class="by">by</span> <a href="/speakers/#leisa">Leisa Reichelt</a></h4>
<h5 class="soldout">Sold out</h5>

<p>It&#8217;s one thing to do user experience design by yourself. Now, take it to another level by using collaborative techniques to get your project team and your client involved productively.  In this super, hands-on workshop you&#8217;ll learn a range of practical techniques that will be great additions to your facilitation and UX toolkit, including:</p>

<ul>
 <li><strong>Brainstorming Techniques that Really Work</strong>. How many brainstorming sessions have you been in that fall completely flat? Or that can't get past the first good idea that someone has? Brainstorming is actually a really powerful tool - learn how to make it work.</li>
 <li class="bul2"><strong>Effective and Fun Collaborative Design Techniques</strong>. Bringing your team and project stakeholders into the design process early helps bring important information onto the table earlier and accelerates solution design.  These techniques make design fun and non-threatening for participants, and more productive for you.</li>
 <li><strong>Techniques for Achieving Consensus</strong>. How do you decide which features go into your products and which don&#8217;t make the cut? How do you agree on hierarchy and prioritisation? There are ways and means of making this painless and this workshop will show you how.</li>
 <li class="bul2"><strong>Building Collaboration into your Project Methodology</strong>. Involving all the key disciplines throughout the project lifecycle is a great way to improve your project outcomes and minimise risk and it makes project more fun to work on. Find out ways that you can integrate collaboration into your project lifecycle.</li>
</ul>


<hr />

<h4 id="peter"><span class="time">Thursday 6th September /</span> Principles &amp; Practices for Successful Experience Design <span class="by">by</span> <a href="/speakers/#peter">Peter Merholz</a></h4>
<h5 class="soldout">Sold out</h5>

<p>For this one-day workshop, Peter Merholz has looked across an entire user experience program, and will present a selection of principles and practices that Adaptive Path uses to deliver great work.</p>

<ul>
 <li class="bul2"><strong>Prioritize business problems</strong>. Too often user experience practitioners are handed a list of requirements and told to build something &ndash; even when we know it&#8217;s not the right thing to build. Peter will share with you a prioritization exercise that Adaptive Path has found very successful in focusing a teams efforts on those things that it can truly affect.</li>
 <li><strong>Persona development</strong>. Yes, yes, yes, we all develop personas. But are you developing personas effectively? Peter will discuss approaches, tips, and tricks that lead to personas that truly work for your design problem, and communicating with your organization.</li>
 <li class="bul2"><strong>Building the information architecture</strong>. Of late, interaction design has ascended, while its sister discipline, information architecture, has waned. Peter will explain why information architecture is as vital now as it ever has been, and will present ways for you to get the most out of your metadata.</li>
 <li><strong>Designing for interaction</strong>. In this Ajax-laden, rich internet application world, designing for interaction has become paramount. Join Peter as he explains traits, characteristics, and approaches to successful interaction design, culminating in design exercises that put these principles to work.</li>
</ul>

<hr />

<h4 id="thomas"><span class="time">Thursday 6th September /</span> Building the Social Web with Tagging/Folksonomies <span class="by">by</span> <a href="/speakers/#thomas">Thomas Vander Wal</a></h4>
<h5 class="soldout">Sold out</h5>

<p>Thomas Vander Wal, creator of the term folksonomy, provides a full-day workshop on building the social web through a detailed look at tagging systems.  The workshop will provide a foundation for understanding social software from the perspective of the people who use it. This perspective helps site owners solve the &#8216;cold start&#8217; problem of social software not starting out social.</p> 

<p>The focus of the workshop is to provide a solid foundation for building and maintaining a social system from the design and management perspective. The workshop will cover policy issues, monitoring, analysis, and tagging systems as features that are added to the mix of existing tools.</p>

<p>The day will provide a brief history of tagging from the days of tagging in the desktop era to current web use. The exercises will focus on better understanding what happens in tagging systems and use those lessons to frame how to build better systems and make better use of the information that is made relevant through those tagging systems. The workshop includes overviews of social web pattern interaction design and the wide array of features.</p>


