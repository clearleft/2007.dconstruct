<h5 class="vevent"><span class="time"><abbr class="dtstart" title="2007-09-07T11:00:00">11:00</abbr>&ndash;<abbr class="dtend" title="2007-09-07T11:30:00">11:30</abbr> /</span> <span class="summary">Break</span> (tea, coffee &amp; biscuits)</h5>

