<h5 class="vevent" id="peter"><span class="time"><abbr class="dtstart" title="2007-09-07T11:30:00">11:30</abbr>&ndash;<abbr class="dtend" title="2007-09-07T12:15:00">12:15</abbr> /</span> <span class="summary">Experience Strategies</span> <span class="by">by</span> <a href="/speakers/#peter">Peter Merholz</a></h5>

<p>Engage in a deep dive into the importance of experience. In this session Peter will cover the historical perspective; lay out the evolution of product categories; demonstrate the competitive advantage that experience provides; explain the importance of thinking about your products in a systems fashion; and cap it off with a discussion of experience strategies and how they can provide stars to sail your ship by.</p>

<hr />

<h5 class="vevent" id="leisa"><span class="time"><abbr class="dtstart" title="2007-09-07T12:15:00">12:15</abbr>&ndash;<abbr class="dtend" title="2007-09-07T13:00:00">13:00</abbr> /</span> <span class="summary">Waterfall Bad, Washing Machine Good</span> <span class="by">by</span> <a href="/speakers/#leisa">Leisa Reichelt</a></h5>

<p>There is a commonly held belief that Agile Methods and User Centred Design do not play nicely together. On the contrary, Agile and UCD have much to offer each other. Each can learn and benefit from the other, and work together as a robust design and development methodology.</p>  

<p>Including an introduction to the principles and practices of Agile Methodologies, this presentation explores the opportunities for UCD in an Agile environment, how designers can shape Agile to better support their work, and what designers can learn from Agile methods.</p>

