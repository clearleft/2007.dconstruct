<h5 class="vevent"><span class="time"><abbr class="dtstart" title="2007-09-07T16:00:00">16:00</abbr>&ndash;<abbr class="2007-09-07T16:30:00">16:30</abbr> /</span> <span class="summary">Break</span> (tea, coffee &amp; biscuits)</h5>

