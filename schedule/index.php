<h5 class="vevent" id="jared"><span class="time"><abbr class="dtstart" title="2007-09-07T10:15:00">10:15</abbr>&ndash;<abbr class="dtend" title="2007-09-07T11:0:000">11:00</abbr> /</span> <span class="summary">The Dawning of the Age of Experience</span> <span class="by">by</span> <a href="/speakers/#jared">Jared Spool</a></h5>

<p>Experience design is no longer a nice-to-have luxury of a few organisations with tons of money and exceptional visionary management. It&#8217;s become commonplace for organisations that build products and websites. Experience Design is a centerpiece of boardroom discussions and quickly becoming a key performance indicator for many businesses.</p> 

<p>However, you can&#8217;t just hire a couple of &#8216;experience designers&#8217; and tell them, &#8220;Go do that voodoo that you do so well.&#8221; Today&#8217;s business environment forces us to build multi-disciplinary teams, compiling a diverse group of skills and experiences to handle the many facets of the technical, business, and user requirements.</p>

<p>In his usual entertaining and insightful manner, Jared will talk about what it takes to build a design team that meets today&#8217;s needs. See how successful experience design integrates the needs of the users with the requirements of the business. See how it is learned, but not available through introspection. Learn how experience design must be invisible to succeed and why it is cultural, multi-disciplinary, and thrives best in an &#8216;educate and administrate&#8217; environment.</p>

<p>You&#8217;ll see examples of designs from Apple&#8217;s iPod, Netflix, the Mayo Clinic, and Southwest Airlines, to name a few.</p>

