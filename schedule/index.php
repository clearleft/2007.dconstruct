<h5 class="vevent"><span class="time"><abbr class="dtstart" title="2007-09-07T10:00:00">10:00</abbr>&ndash;<abbr class="dtend" title="2007-09-07T10:15">10:15</abbr> /</span> <span class="summary">Opening remarks</span></h5>

