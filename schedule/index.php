<h5 class="vevent" id="matt"><span class="time"><abbr class="dtstart" title="2007-09-07T16:30:00">16:30</abbr>&ndash;<abbr class="dtend" title="2007-09-07T17:15:00">17:15</abbr> /</span> <span class="summary">The Experience Stack</span> <span class="by">by</span> <a href="/speakers/#matt">Matt Webb</a></h5>

<p>Here are two ways of looking at a television: a TV is a display surface in my home which can show video which is broadcast or kept on storage media. And then: television is a friend who starts conversations between me and other people.</p> 

<p>Products aren't only their aesthetic form and feature lists in catalogues. We live alongside them, and they open us to experiences. We first spy them across a crowded shop floor (then take them home and unwrap them); we get to know them, are frustrated by them, are pleased by them; we socialise with them and our other friends.</p>

<p>The <em>experience</em> of a product is what we feel and what guides us through our lives together. Every time we cross paths, there&#8217;s a hook for experience. The sequence of these communicates the brand, and can be variously playful, engaging, educational or however we choose to colour it.</p>

<p>Being aware of how this happens helps us design that experience. Through his favourite on-screen apps and physical, plastic gadgets, Matt looks at the whole experience stack &ndash; from the moment-by-moment feedback in user interface to large, complex ideas in critical design &ndash; and discusses how we can apply these ideas to our own projects.</p>

<hr />

<h5 class="vevent" id="tom"><span class="time"><abbr class="dtstart" title="2007-09-07T17:15:00">17:15</abbr>&ndash;<abbr class="dtend" title="2007-09-07T18:00:00">18:00</abbr> /</span> <span class="summary">Designing for a Web of Data</span>  <span class="by">by</span> <a href="/speakers/#tom">Tom Coates</a></h5>

<p>Design patterns for brochureware and editorial sites are well-established. In fact, they&#8217;re so simple and formulaic that even waterfall development processes can churn them out. A producer has an idea, a designer mocks it up in Photoshop and then client side types and engineers go all agile on its ass.</p>

<p>But what happens when you&#8217;re pushing into web apps or social media? What happens when an absence of heirarchy makes left-hand navigation redundant? What do you do when design practice blurs into URLs and data structures, and where your service breaks the frame of the browser and starts appearing in hardware, in desktop applications or on other people&#8217;s sites?</p>

<p>In this session, Tom will talk about new literacies that designers need to build things that are native to a web of data, the blurring and interplay between designers and developers and what it means to rapidly iterate in small multi-disciplinary teams to find the heart and soul of a new concept.
</p>

