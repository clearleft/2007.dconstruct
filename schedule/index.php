<h5 class="vevent" id="cameron"><span class="time"><abbr class="dtstart" title="2007-09-07T14:30:00">14:30</abbr>&ndash;<abbr class="dtend" title="2007-09-07T15:15:00">15:15</abbr> /</span> <span class="summary">Good vs. Great Design</span> <span class="by">by</span> <a href="/speakers/#cameron">Cameron Moll</a></h5>

<p>It&#8217;s easy to poke fun at bad design, but it&#8217;s far more challenging and rewarding to discern differences between good design and great design. This session will teach you practical design techniques for tipping the scales of greatness in your favor, using a blend of graphic design theory, human computing principles, and a communication-centric approach. Learn the difference between concepts such as influence vs. inspiration or machine efficiency vs. user efficiency.</p>

<hr />

<h5 class="vevent" id="georgedenise"><span class="time"><abbr class="dtstart" title="2007-09-07T15:15:00">15:15</abbr>&ndash;<abbr class="dtend" title="2007-09-07T16:00:00">16:00</abbr> /</span> <span class="summary">Human Traffic</span> <span class="by">by</span> <a href="/speakers/#george">George Oates</a> <span class="by">&amp;</span> <a href="/speakers#denise">Denise Wilton</a></h5>

<p>Denise and George want to talk to you about a few community-based projects they&#8217;ve worked on. They&#8217;ll delve into strategies and ideas around building a sense of place, and discuss the dynamics they have observed by watching people get comfortable&#8230; so comfortable there&#8217;s a sense of ownership and possession.</p>

