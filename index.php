<div class="hentry">
	<h4 class="entry-title">dConstruct Workshops</h4>
	<h5><abbr class="updated" title="2007-09-17T14:30:00">September 17th</abbr></h5>
	<div class="entry-content">

<p>One of the biggest successes of dConstruct 2007 this year were the pre-event workshops. These workshops were small, intimate affairs, so were heavily oversubscribed and sold out quickly. As well as assuring people a place at the conference itself, the workshops provided a great opportunity for people to get the most value from our great line-up of speakers. The workshops were also an excellent way of meeting people and networking before the main event. In fact, one of the most common bits of feedback we got from dConstruct were people expressing an interest in attending the workshops next year.</p>

<p>The sessions themselves were both enlightening and extremely practical. They ranged from theoretical discussions around the <a href="http://2007.dconstruct.org/workshops/#thomas">benefits and implementation of tagging systems</a>, through to hands-on workshops on the <a href="http://2007.dconstruct.org/workshops/#leisa">creativity</a> and <a href="http://2007.dconstruct.org/workshops/#peter">principles</a> of user experience design, replete with group exercises and lots and lots of sticky notes. The feedback was universally positive and everybody we spoke to had a great time.</p>

<p>These sessions were always intended to be one off events, closely tied to the main conference. However we&#8217;ve already had a number of people asking if we can run some of these sessions again. So rather than disappoint people, we thought we&#8217;d ask your opinion. Drop us an <a href="mailto:info@clearleft.com">email</a> if you&#8217;d like us to run one of the workshops again, at the start of December. If we get 10 or more people interested in a particular workshop, we&#8217;ll try to arrange a re-run. </p>

	</div>
</div>

