		<p>To follow the reaction to dConstruct, check out the <a href="http://dconstruct07.backnetwork.com/feeds/">blog posts</a> collated on the <a href="http://dconstruct07.backnetwork.com/">Backnetwork</a>, as well as the <a href="http://www.flickr.com/search/?q=dconstruct07">photos</a> in the <a href="http://www.flickr.com/groups/423467@N23/pool/">Flickr pool</a>.</p>
		
